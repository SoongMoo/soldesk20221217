package methodTest;

public class MethodEx06 {

	public static void main(String[] args) {
		int x = 10;
		int y = 5;
		int result = 0;
		result = x + y;
		System.out.println("x + y = " + result);
		result = x - y;
		System.out.println("x - y = " + result);
		result = x * y;
		System.out.println("x * y = " + result);
		result = x / y;
		System.out.println("x / y = " + result);

	}

}
