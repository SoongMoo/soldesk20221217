package operationTest;

public class OperateEx2 {
	public static void main(String[] args) {
		// 대입 연산자.
		int i = 10;
		System.out.println(i);
		i += 2; //i = i + 2; // i = 10 + 2
		System.out.println(i);
		int y = 10;
		y = y + 5;
		System.out.println(y);
		y -= 5; // y = y - 5;
		System.out.println(y);
		// +=, -=, /= , *= , %=
		i = 10;
		i %= 3; // i = i % 3;
		System.out.println(i);
		
		
		
	}
}
