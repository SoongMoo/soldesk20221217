package object2Test;

public class Human {
	
	int age;
	float heught;
	String name;
	String addr;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public float getHeught() {
		return heught;
	}
	public void setHeught(float heught) {
		this.heught = heught;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}

	
}
